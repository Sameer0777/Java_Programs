package Example;

class Address {
	int housenum;
	String streetname;
	String areaname;
	String city;
	String state;
	int pincode;
	
	Address(int housenum, String streetname, String areaname, String city, String state, int pincode) {
		this.housenum = housenum;
		this.streetname = streetname;
		this.areaname = areaname;
		this.city = city;
		this.state = state;
		this.pincode = pincode;
	}
	
}
